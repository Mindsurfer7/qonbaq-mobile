/// Контекст использования голосового сообщения
/// 
/// Определяет, для какой цели используется голосовое сообщение
/// и какой endpoint должен быть вызван для его обработки
enum VoiceContext {
  /// Простая транскрипция голоса в текст (для чата, заметок)
  transcription,

  /// Создание задачи с предзаполнением формы
  task,

  /// Создание согласования с предзаполнением формы
  approval,

  /// Страница "Не забыть выполнить"
  dontForget,
}

